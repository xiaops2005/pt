package com.viewhigh.excel.domain.dict;
/**
 * 数字财务分析表
 * @author xiaops
 *
 */
public class ReportNumber {
	//项目
	private String itemName;
	//单位
	private String unitName;
	//期末数
	private String finalNum;
	//上年同期数
	private String lastYearNum;
	//本月止累计数
	private String monthEndNum;
	//项目
	private String itemNameOther;
	//单位
	private String unitNameOther;
	//期末数
	private String finalNumOther;
	//比上年同期增
	private String lastYearOther;
	public String getItemName() {
		return itemName;
	}
	public void setItemName(String itemName) {
		this.itemName = itemName;
	}
	public String getUnitName() {
		return unitName;
	}
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	public String getFinalNum() {
		return finalNum;
	}
	public void setFinalNum(String finalNum) {
		this.finalNum = finalNum;
	}
	public String getLastYearNum() {
		return lastYearNum;
	}
	public void setLastYearNum(String lastYearNum) {
		this.lastYearNum = lastYearNum;
	}
	public String getMonthEndNum() {
		return monthEndNum;
	}
	public void setMonthEndNum(String monthEndNum) {
		this.monthEndNum = monthEndNum;
	}
	public String getItemNameOther() {
		return itemNameOther;
	}
	public void setItemNameOther(String itemNameOther) {
		this.itemNameOther = itemNameOther;
	}
	public String getUnitNameOther() {
		return unitNameOther;
	}
	public void setUnitNameOther(String unitNameOther) {
		this.unitNameOther = unitNameOther;
	}
	public String getFinalNumOther() {
		return finalNumOther;
	}
	public void setFinalNumOther(String finalNumOther) {
		this.finalNumOther = finalNumOther;
	}
	public String getLastYearOther() {
		return lastYearOther;
	}
	public void setLastYearOther(String lastYearOther) {
		this.lastYearOther = lastYearOther;
	}
	
}
